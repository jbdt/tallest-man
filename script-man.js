function colorizeContent(content) {
    const colorMapping = {
        '░░': 'color-orange',
        '▒▒': 'no-color',
        '▓▓': 'color-blue',
        '██': 'color-darkblue',
        ';;': 'color-darkblue2',
        '::': 'color-darkgreen',
        '_': 'color-white',
        '/': 'color-white',
        '.': 'color-white',
        '\\': 'color-white',
        '|': 'color-white',
        '0': 'color-white',
        '1': 'color-white',
        '2': 'color-white',
        'm': 'color-white',
    };

    return content.replace(/░░|▒▒|▓▓|██|;;|::|_|\/|\.|\\|\||[0-2]|m/g, match => {
        return `<span class="${colorMapping[match]}">${match}</span>`;
    });
}

